package com.company.day3;

public class WhileLoopExample {
	
	public static void main(String[] args) {
		
		int num = 10;
		
		while(num > 1 || num > 5) {
			System.out.println(num);
			num--;
		}
		
		int arr[] = {1, 11, 45, 9};
		
		int i = 0;
		
		while (i < 4) {
			System.out.println(arr[i]);
			i++;
		}
		
		
	}

}
