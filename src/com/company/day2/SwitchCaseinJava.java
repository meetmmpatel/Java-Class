package com.company.day2;

public class SwitchCaseinJava {

	public static void main(String[] args) {

		int num = 2;

		switch (num + 1) {
		case 1:
			System.out.println("Case number 1: " + num);
			
		case 2:
			System.out.println("Case number 2" + num);
			
		case 3:
			System.out.println("Case3: number 3 " + num);
			
			
			default:
				System.out.println("Default");
		}
			

	}

}
